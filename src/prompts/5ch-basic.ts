const JSON_INSTRUCTION =
  "\n【重要】出力は必ず、以下のキーを持つ有効なJSON形式（Markdownのコードブロックを含まない純粋なJSON文字列）のみを出力してください。キーの構成は適宜最適なものを設計してください。";

export function build5chBasicPrompt(text: string, isJson: boolean = false): string {
  return `
以下の【対象テキスト】に含まれる5ちゃんねるスレッドの全レスを読み込み、まとめサイトの記事としてそのまま使える形式で出力してください。

【前提条件（ノイズ排除）】
主旨と無関係なコピペ、陰謀論、過激な政治的罵詈雑言、荒らし、単なる誹謗中傷は徹底的に排除してください。

【出力要件】
1. 抽出とHTML化
スレッド内の「面白いツッコミ」「役立つ情報」「議論の核心を突くレス」を15〜20個厳選し、会話の流れがわかるように時系列で並べてください。
出力の際は、以下のHTML形式でレスを装飾してください。
<div class="res-block">
  <span class="res-num">対象のレス番号</span>：<span class="res-name">名無しさん</span>
  <p class="res-text">レスの本文</p>
</div>

2. バズるタイトル案
抽出した内容をもとに、SNS（Xなど）でクリックしたくなるような、煽りすぎないが興味を惹くキャッチーな記事タイトル案を5つ提示してください。

3. 管理人のひとこと
記事の末尾に配置する「まとめサイトの管理人」としてのコメントを、少し皮肉を交えつつも的を射たトーンで、150文字程度で生成してください。

【対象テキスト】
${text}${isJson ? JSON_INSTRUCTION : ""}
`;
}
