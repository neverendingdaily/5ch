【前提条件（ガルちゃん特有のノイズ排除）】
以下の書き込みは排除してください。
・男女対立を煽る書き込み、特定の年代や職業への根拠のないヘイト
・男性と推測されるユーザーからの説教や荒らし
・トピックの主旨から外れた、トピ主への過剰な個人攻撃（主叩き）

【出力要件】
A. 【圧倒的共感】
トピック内で最も多くの人が賛同している「核となる意見・価値観」を3〜5つ、箇条書きで要約してください。なぜそれに共感しているのかも添えてください。

